# app/main.py
import uvicorn
from . import app # Импортируем экземпляр 'app' из __init__.py текущего пакета

if __name__ == "__main__":
    # Запускаем FastAPI-приложение с помощью Uvicorn
    # "app.main:app" означает:
    # - "app": имя пакета (ваша папка 'app')
    # - "main": имя модуля, в котором находится приложение (этот файл main.py)
    # - "app": имя переменной, которая является экземпляром FastAPI (определена в __init__.py)
    #
    # Важно: Если вы запускаете из корневой директории проекта (METEOGUESS/),
    # то команда будет `uvicorn app.main:app --reload`.
    #
    # Если вы хотите, чтобы uvicorn напрямую использовал экземпляр из __init__.py,
    # то можно указать "app:app" (без .main). Это более распространенный подход,
    # когда __init__.py является основной точкой инициализации.
    # В этом случае `main.py` может быть даже не нужен для запуска,
    # а только для отладки или специфических сценариев.
    uvicorn.run("app:app", host="0.0.0.0", port=8000, reload=True)